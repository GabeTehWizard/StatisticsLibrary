        public static List<double> GetZScoreByValue(double value)
        {
            if (!ZTableByValue.ContainsKey(value))
                throw new ArgumentOutOfRangeException("Value does not exist in z table.");
            return ZTableByValue[value];
        }

        public static string GetZScoreByValueAsString(double value)
        {
            return string.Join(", ", GetZScoreByValue(value).Select(x => x.ToString()).ToList());
        }

        public static double GetValueByZScore(double zScore)
        {
            if (!ZTableByZScore.ContainsKey(zScore))
                throw new ArgumentOutOfRangeException("zScore",zScore, zScoreOutOfRangeMessage);
            return ZTableByZScore[zScore];
        }

        public static double GetZScoreProbability(double zScore)
        {
            if (zScore < 0) return 1 - GetValueByZScore(zScore) - .50;
            else return GetValueByZScore(zScore) - .50;
        }

        public static double CalculateNForGivenCofidenceInterval(double confidenceInterval, double standardDeviation, double errorRange)
        {
            return RoundUp(Math.Pow(FindZScoreByRelativeValue(confidenceInterval / 2 + .5) * standardDeviation / errorRange, 2),0);
        }

        public static double CalculateZScoreProbabilityRange(double min, double max, double mean, double standardDeviation)
        {
            double probability1 = GetZScoreProbability(CalculateZ(min, mean, standardDeviation));
            double probability2 = GetZScoreProbability(CalculateZ(max, mean, standardDeviation));

            return min < mean && max < mean ? Round(probability1 - probability2,4) : min > mean && max > mean ? Round(probability2 - probability1,4) : Round(probability1 + probability2,4);
        }

        public static double CalculateTailProbability(double max, double mean, double standardDeviation)
        {
            double probability1 = GetZScoreProbability(CalculateZ(max, mean, standardDeviation));
            return .5 - probability1;
        }

        public static double CalculateTailComplementProbability(double max, double mean, double standardDeviation)
        {
            return 1 - CalculateTailProbability(max, mean, standardDeviation);
        }

        public static double FindZScoreByRelativeValue(double value)
        {
            if (!ZTableByValue.ContainsKey(value))
            {
                double closestKey = 0;
                double difference = double.MaxValue;
                double previous;
                foreach (double key in ZTableByValue.Keys)
                {
                    previous = difference;
                    difference = Math.Abs(key - value);

                    if (previous == difference)
                    {
                        return (ZTableByValue[closestKey].First() + ZTableByValue[key].First()) / 2;
                    }
                    else if (previous < difference)
                    {
                        if (ZTableByValue[closestKey].Count > 1 || ZTableByValue[key].Count > 1) return (ZTableByValue[closestKey].Average() + ZTableByValue[key].Average()) / 2;
                        return ZTableByValue[closestKey].Average();
                    }
                    closestKey = key;
                }
            }
            if (ZTableByValue[value].Count > 1) return ZTableByValue[value].Average();
            return ZTableByValue[value].First();
        }

        public static double CalculateZ(double x, double mean, double standardDeviation)
        {
            return Round((x - mean) / standardDeviation, 2);
        }

        public static double EstimateMeanAboveFromSamplePopulation(double xBar, double mu, double sigma, double n)
        {
            return GetZScoreProbability(Round((xBar - mu) / (sigma / Math.Sqrt(n)),2));
        }

        public static double EstimateMeanBelowFromSamplePopulation(double xBar, double mu, double sigma, double n)
        {
            return .5 - GetZScoreProbability(Round((xBar - mu) / (sigma / Math.Sqrt(n)), 2));
        }

        public static double SolveForX(double z, double mean, double standardDeviation)
        {
            return z * standardDeviation + mean;
        }

        public static List<double> SolveForXRange(double minPercent, double maxPercent, double mean, double standardDeviation)
        {
            return new List<double> { Round(SolveForX(FindZScoreByRelativeValue(minPercent), mean, standardDeviation),4), Round(SolveForX(FindZScoreByRelativeValue(maxPercent), mean, standardDeviation),4) };
        }

